// Человеческие подписи статусов для личного кабинета (SPEC.md §4.4: статус-бейдж
// понятным языком). Первоисточник — `meta.labels_plain` из Definition услуги
// (приходит в каждом элементе GET /api/v1/applications); словарь ниже — запасной
// вариант для статусов, которым организация не дала свою подпись.

const FALLBACK_LABELS: Record<string, string> = {
  draft: "Черновик",
  submitted: "Заявка отправлена",
  registered: "Заявка зарегистрирована",
  in_review_bpm: "Заявку рассматривает организация",
  docs_requested: "Нужны дополнительные документы",
  approved: "Заявка одобрена",
  rejected: "Отказано",
};

export function statusLabel(status: string, labelsPlain?: Record<string, string>): string {
  return labelsPlain?.[status] ?? FALLBACK_LABELS[status] ?? status;
}

// Блок «Что дальше» на детальной странице: короткая подсказка без канцелярита,
// что произойдёт после текущего статуса.
export function nextSteps(status: string): string[] {
  if (status === "draft") {
    return [
      "Дозаполните заявку — всё введённое уже сохранено.",
      "Проверьте данные на экране «Проверка и отправка» и отправьте заявку.",
    ];
  }
  if (status === "approved") {
    return ["Решение принято в вашу пользу. Организация свяжется с вами по дальнейшим шагам."];
  }
  if (status === "rejected") {
    return [
      "По этой заявке принято отрицательное решение.",
      "Можно подобрать другую программу в каталоге и подать новую заявку.",
    ];
  }
  // submitted / registered / in_review_bpm / docs_requested и любые новые статусы рассмотрения.
  return [
    "Организация рассматривает заявку в срок, указанный на карточке услуги.",
    "Если понадобятся дополнительные документы, здесь появится запрос на дозагрузку.",
    "Каждое изменение статуса будет видно на этой странице.",
  ];
}
